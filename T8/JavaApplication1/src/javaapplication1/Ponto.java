public class Ponto {
    
    public static double x = 0;
    public static double y = 0;
    
    public Ponto()
    { 
        x = 0.0;
        y = 0.0;
    
        
    }
    
    public Ponto(double _x, double _y)
    {
        x += _x;
        y += _y;
        
    
    }

   
    
}
